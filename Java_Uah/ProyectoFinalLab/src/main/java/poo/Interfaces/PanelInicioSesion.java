    public static void guardarDniTemp(String dni) {
        try (PrintWriter salida= new PrintWriter(new BufferedWriter(new FileWriter("ficheros/archivoTemporal.dat")))) {
            salida.println(dni);
            
        } catch (IOException ioe) {
            System.out.println("Error IO: "+ioe.toString());
        }
    }
    

    
