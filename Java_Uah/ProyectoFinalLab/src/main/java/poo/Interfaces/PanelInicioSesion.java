    @SuppressWarnings("unchecked")
    private void btnConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConfirmarMouseClicked
                
        if (admin.getCorreo().equals(txtCorreo.getText()) && admin.getClave().equals(txtContrasena.getText())) {
            
            PanelAdmin pa = new PanelAdmin();
            pa.setVisible(true);
            this.dispose();
            
        } else{
            
            CorreoInvalido.setVisible(true);
            ContrasenaInvalido.setVisible(true);
        }

        GuardarLeerFicheros.cargarDatosClientes();
        
        clientes = GuardarLeerFicheros.obtenerArrayClientes(); 
        
        for(Cliente cli:clientes){                              // Comprobar que el usuario y contraseña son validos

            if (cli.getCorreo().equals(txtCorreo.getText()) && cli.getClave().equals(txtContrasena.getText())){  // Comprobar que el correo y contraseña coinciden
                
                guardarDniTemp(cli.getDni());

                PanelUsuario pu = new PanelUsuario();           // Se le permite acceder al panel de usuario
                this.dispose();                                 // Ocultamos el panel de inicio
            } else{

                CorreoInvalido.setVisible(true);
                ContrasenaInvalido.setVisible(true);
            }
        }

    }//GEN-LAST:event_btnConfirmarMouseClicked

