    public static String leerDniTemp() {
        String dni = "";
        try {
            BufferedReader br = new BufferedReader(new FileReader("ficheros/archivoTemporal.dat"));
            String temp = "";
            String bfRead;
            while ((bfRead = br.readLine()) != null) {
                temp = temp + bfRead;
            }
            dni = temp;
            
        } catch (Exception e) {
        }
        return dni;
    }
    
    public void ordenarAnno() {
        Collections.sort(series, new Comparator<Serie>() {            
            @Override
            //Ordenamos el array series por año, de mayor a menor
            public int compare(Serie p1, Serie p2) {
                    //Comparamos p2 con p1
                    return new Integer(Integer.parseInt(p2.getAnno())).compareTo(new Integer(Integer.parseInt(p1.getAnno())));
            }
        });
    }
    
    public void ordenarPuntuacion() {
        Collections.sort(series, new Comparator<Serie>() {            
            @Override
            //Ordenamos el array series por puntuación, de mayor a menor
            public int compare(Serie p1, Serie p2) {
                    //Comparamos p2 con p1
                    return new Double(Double.parseDouble(p2.getPuntuacion())).compareTo(new Double(Double.parseDouble(p1.getPuntuacion())));
            }
        });
    }


