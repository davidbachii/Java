    private void btnAddFavMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddFavMouseClicked
        // TODO add your handling code here:

        int selection = listaBusqueda.getSelectedIndex();
        if (selection != -1) {
            Serie s = series.get(selection);
            
            try {
            
            File file1 = new File("ficheros/listaFav/listaSeriesFav"+leerDniTemp()+".dat");
            
            // Si los archivos no existen se crean
                if (!file1.exists()) {
                file1.createNewFile();
            }
            } catch (IOException e) {
            e.printStackTrace();
            }
            
            if (seriesFav.isEmpty()) {
                    seriesFav.add(s);
                    ordenarAnno();
                    GuardarLeerFicheros.guardarSerieListaFav(leerDniTemp());
            }
            
            for (Serie ser: seriesFav) {
                
                if (!ser.getTitulo().equals(s.getTitulo())) {
                    seriesFav.add(s);
                    ordenarAnno();
                    GuardarLeerFicheros.guardarSerieListaFav(leerDniTemp());
                }
            }

        }
    }//GEN-LAST:event_btnAddFavMouseClicked

