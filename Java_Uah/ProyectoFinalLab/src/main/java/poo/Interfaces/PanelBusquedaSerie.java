    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String busqueda =jTextField1.getText().toLowerCase();

        modelo.removeAllElements();
        for(int i=0;i<series.size();i++){
            if((jComboBox1.getSelectedItem().equals("Sin filtro"))&&(series.get(i).getTitulo().toLowerCase().contains(busqueda))){
                modelo.addElement(series.get(i).getTitulo());
            }
            else if((jComboBox1.getSelectedItem().equals("Género"))&&(series.get(i).getGenero().toLowerCase().contains(busqueda))){
                modelo.addElement(series.get(i).getTitulo());
            }
            else if((jComboBox1.getSelectedItem().equals("Actores"))&&(series.get(i).getActores().toLowerCase().contains(busqueda))){
                modelo.addElement(series.get(i).getTitulo());
            }
            else if((jComboBox1.getSelectedItem().equals("Año"))&&(series.get(i).getAnno().toLowerCase().contains(busqueda))){
                modelo.addElement(series.get(i).getTitulo());
            }
        
        }        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void listaBusquedaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaBusquedaValueChanged

        int selection = listaBusqueda.getSelectedIndex();       
        modelo2.removeAllElements();       
        while ((selection != -1)&&(selection<series.size())) {
            Serie s = series.get(selection);
            if(!s.getTitulo().contains(jTextField1.getText().toLowerCase())){
                System.out.print("Entra al if");
                selection++;                
            }
            else{
                System.out.print("Entra al else");
               
                            
                capitulos=s.getCapitulos();            
                jSinopsis.setText("Sinopsis: "+"\n"+s.getSinopsis());            
                jImagen.setIcon(s.getImagen());
                jActor.setText("Actores: "+s.getActores());
                jAnno.setText("Año: "+s.getAnno());
                jTemporada.setText("Nº temporadas: "+s.getTemporadas());
                jGenero.setText("Género: "+s.getGenero());
                jTitulo.setText("Titulo: "+s.getTitulo());  
                jPuntuacion3.setText("Puntuación: " + s.getPuntuacion());
                for(Capitulo c:capitulos){
                  modelo2.addElement(c.getTitulo());                
                }
                selection++;   
                    }
    
        }
        
    }//GEN-LAST:event_listaBusquedaValueChanged

