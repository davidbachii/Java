    public static boolean validarSaldo() {
        return sus.getCliente().getTarjetaCredito().getSaldo() >= sus.getPrecioSuscripcion();
    }
    
    public static boolean validarCaducidad() {
        return sus.getCliente().getTarjetaCredito().getFechaCaducidad().isAfter(sus.getFechaSuscripcion());
    }
    
    public static String leerDniTemp() {
        String dni = "";
        try {
            BufferedReader br = new BufferedReader(new FileReader("ficheros/archivoTemporal.dat"));
            String temp = "";
            String bfRead;
            while ((bfRead = br.readLine()) != null) {
                temp = temp + bfRead;
            }
            dni = temp;
            
        } catch (Exception e) {
        }
        return dni;
    }

