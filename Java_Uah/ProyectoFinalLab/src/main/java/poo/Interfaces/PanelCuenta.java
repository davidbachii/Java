    private void btnPagoSusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPagoSusMouseClicked
        // TODO add your handling code here:
        Double saldoInicial = sus.getCliente().getTarjetaCredito().getSaldo();
        
        System.out.println(validarCaducidad());
        System.out.println(validarSaldo());
        
        if (validarCaducidad() && validarSaldo()) {
            
            
            sus.getCliente().getTarjetaCredito().setSaldo(saldoInicial-sus.getPrecioSuscripcion());
            GuardarLeerFicheros.guardarDatosSuscripcion(sus.getCliente().getDni(), sus);
            
            GuardarLeerFicheros.cargarDatosClientes();
            clientes = GuardarLeerFicheros.obtenerArrayClientes();
            
            for(Cliente cli:clientes){
                if (cli.getDni().equals(sus.getCliente().getDni())){
                    cli.getTarjetaCredito().setSaldo(saldoInicial-sus.getPrecioSuscripcion());
                    
                }
            }
            GuardarLeerFicheros.guardarDatosClientes();
            
        }
        
        btnPagoSus.setEnabled(false);
    }//GEN-LAST:event_btnPagoSusMouseClicked

