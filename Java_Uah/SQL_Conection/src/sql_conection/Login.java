    private void btnConfirmar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConfirmar1MouseClicked
 
        user = txtCorreo1.getText();
        pass = txtContraseña.getText();
        PanelConsultas pc = new PanelConsultas(this.user,this.pass);
        
        //Aqui seleccionamos el user que ha introducido, 
        switch (user) {
            case "administrador"://CASO ADMINISTRADOR
                if (pass.equals("admin")) {
                    esCorrecto(user, pass);
                    
                   
                    pc.setVisible(true);
                     this.dispose();
                } else {
                    esIncorrecto();
                    
                    this.dispose();
                }
                break;
            case "gestor"://CASO GESTOR
                if (pass.equals("gestor")) {
                    esCorrecto(user, pass);
                   
                   
                    
                     this.dispose();
                     pc.setVisible(true);
                } else {
                    esIncorrecto();
                    this.dispose();
                    
                }
                break;
            case "invitado"://CASO RECEPCIONISTA
                if (pass.equals("invitado")) {
                    esCorrecto(user, pass);
                    
                    this.dispose();
                    pc.setVisible(true);
                } else {
                    esIncorrecto();
                    
                    this.dispose();
                }
                break;
            case "cliente"://CASO GENERAL
                if (pass.equals("cliente")) {
                    esCorrecto(user, pass);
                   
                    
                    pc.setVisible(true);
                    this.dispose();
                } else {
                    esIncorrecto();
                   
                    this.dispose();
                }
                break;
            default://LA MAYORIA DE LOS CASOS SON
                esIncorrecto();
                
               
                break;
        
                                                    
        }
       
    }//GEN-LAST:event_btnConfirmar1MouseClicked

