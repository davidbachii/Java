    public void esCorrecto(String user,String  pass) {
        if(user.equals("postgres")){
            
            this.setUser(user);
            this.setPass(pass);
            
            
        }else{
            
            this.setUser(user);
            this.setPass(pass);
            
            
        }
    }
    
    public void esIncorrecto() {//Si el inicio de sesion no es correcto
        
        CorreoInvalido.setVisible(true);
        ContrasenaInvalido.setVisible(true);
        
        
    }

